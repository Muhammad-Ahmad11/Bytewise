void main() {
  int a = 5;
  int b = 10;
  print(a + b); // Addition
  print(a - b); // Subtraction
  print(a * b); // Multiplication
  print(a / b); // Division
  print(a % b); // Modulus
  print(a > b); // Greater than
  print(a < b); // Less than
  print(a == b); // Equal to
  print(a != b); // Not equal to
  print(a & b); // Bitwise AND
  print(a | b); // Bitwise OR
}
